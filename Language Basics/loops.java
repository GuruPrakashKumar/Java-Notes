// [java holds same syntax as C++ of FOR LOOPS,WHILE LOOPS and DO-WHILE LOOPS]
public class loops {
    public static void main(String[] args) {
        // Print 0 to 10 using for loops. 
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        
        // Print 0 to 10 using while loops. 
        int j=0;
        while (j<=10) {
            System.out.println(j);
            j++;    
        }
        // Print 0 to 10 using do-while loops. 
        int k=0;
        do {
            System.out.println(k);
            k++;
        } while (k<=10);


        }
    }

